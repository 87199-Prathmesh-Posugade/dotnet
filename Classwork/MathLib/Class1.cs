namespace MathLib
{
    public class Maths
    {
        public int Add(int x, int y)
        {
            return x + y;
        }
        public int Sub(int x, int y)
        {
            return x - y;
        }
        public double Mul(double x, double y) { 
            return x * y;
        }
        public double Div(double x, double y) { 
            return x / y;
        }
    }
}
